  glyph=$(get_glyph "$char")
  len=${#glyph}
  # cpr: characters per row
  cpr=$((len / 4))
  [ $cpr -eq 0 ] && cpr=1

  prefix=" "
  [ "$i" == 0 ] && prefix=""
  [ "$prev_char" == "/" ] && [ "$char" == "/" ] && prefix=""
  [ "$prev_char" == "-" ] && [ "$char" == "-" ] && prefix=""
  [ "$prev_char" == "r" ] && [ "$char" == "j" ] && prefix=""
  [ "$prev_char" == "r" ] && [ "$char" == "a" ] && prefix=""
  [ "$prev_char" == "6" ] && [ "$char" == "7" ] && prefix=""
  [ "$prev_char" == "1" ] && [ "$char" == "7" ] && prefix=""
  [ "$prev_char" == "1" ] && [ "$char" == "4" ] && prefix=""

