  r2="$r2$prefix${glyph:$cpr:$cpr}"
  r3="$r3$prefix${glyph:$((cpr * 2)):$cpr}"
  r4="$r4$prefix${glyph:$((cpr * 3)):$cpr}"

  prev_char="$char"
  i=$((i + 1))
done

